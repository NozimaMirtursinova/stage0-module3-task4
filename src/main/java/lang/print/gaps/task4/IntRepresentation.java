package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char firstLetter = 'c';
        int c = firstLetter;
        System.out.println(c);
        char secondLetter = 'h';
        int h = secondLetter;
        System.out.println(h);
        char thirdLetter = 'a';
        int a = thirdLetter;
        System.out.println(a);
        char forthLetter = 'r';
        int r = forthLetter;
        System.out.println(r);
    }
}
